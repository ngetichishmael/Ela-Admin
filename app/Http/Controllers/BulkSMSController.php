   * @return \Illuminate\Http\Response
   */
  public function store(BulkSMSRequest $request)
  {
    $request->validated();

    $customers = Customer::all();
    foreach ($customers as $customer) {
      dispatch(new SendSMS($customer, $request->text));
    }

    return redirect()->route('customers');
  }

